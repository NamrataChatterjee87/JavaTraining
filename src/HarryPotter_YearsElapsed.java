
public class HarryPotter_YearsElapsed {

	public static void main(String[] args) {
		
		
		
		//Harry Potter first movie released
		int firstReleaseYear = 2001;
		
		//Harry Potter last movie released
		int lastReleaseYear = 2011;
		
		//Years elapsed = Last year of release - First year of release
		int yearsElasped = lastReleaseYear - firstReleaseYear ;
		
		System.out.println("Years elapsed between first and last movie of Harry Potter is :" +yearsElasped );
					

	}

}
